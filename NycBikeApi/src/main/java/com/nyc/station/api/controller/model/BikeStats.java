package com.nyc.station.api.controller.model;

public class BikeStats {

	private Integer disableBikes;
	private Integer bikeRides;
	
	public Integer getDisableBikes() {
		return disableBikes;
	}
	public void setDisableBikes(Integer disableBikes) {
		this.disableBikes = disableBikes;
	}
	public Integer getBikeRides() {
		return bikeRides;
	}
	public void setBikeRides(Integer bikeRides) {
		this.bikeRides = bikeRides;
	}
	
	
}
